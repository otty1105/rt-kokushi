-- ============================================================
-- schools テーブル（診療放射線技師養成校マスター）
-- 出典: 第78回診療放射線技師国家試験 学校別合格者状況（厚生労働省）
-- ============================================================
CREATE TABLE IF NOT EXISTS schools (
  id            serial  PRIMARY KEY,
  name          text    NOT NULL,
  program_years integer NOT NULL CHECK (program_years IN (3, 4))
);

-- profiles.school_id に FK 制約を追加
ALTER TABLE profiles
  ADD CONSTRAINT profiles_school_id_fkey
  FOREIGN KEY (school_id) REFERENCES schools(id) ON DELETE SET NULL;

-- RLS（全ユーザー参照可能な公開マスター）
ALTER TABLE schools ENABLE ROW LEVEL SECURITY;

CREATE POLICY "schools_select_all"
  ON schools FOR SELECT USING (true);

GRANT SELECT ON public.schools TO authenticated, anon;

